"""FOIA Automation — sunlight is the best disinfectant.

Generates, tracks, and follows up on Freedom of Information Act
requests to federal and state environmental agencies.

When Raíz finds a facility with violations but no inspection
records, or a permit with no public documentation, it generates
a FOIA request, tracks the response deadline, and flags
stonewalling when agencies don't respond within the legally
required timeframe.

Federal FOIA: 20 business days to respond (can extend 10 more).
State FOIA: varies by state (typically 5-30 days).

Raíz doesn't file the requests itself — it generates the letter,
tells the community member where to send it, and tracks the clock.
The human signs the letter. Raíz remembers the deadline.
"""
from __future__ import annotations

import logging
from dataclasses import dataclass, field
from datetime import datetime, timedelta, timezone
from typing import Any

logger = logging.getLogger(__name__)


@dataclass
class FoiaRequest:
    """A FOIA request generated by Raíz."""
    request_id: str
    target_agency: str
    target_address: str
    target_email: str = ""
    subject: str = ""
    records_requested: list[str] = field(default_factory=list)
    facility_name: str = ""
    facility_id: str = ""
    date_range: str = ""
    generated_date: str = ""
    filed_date: str = ""
    response_deadline: str = ""
    status: str = "draft"  # draft, filed, acknowledged, responded, overdue, appealed
    response_summary: str = ""
    fee_waiver_requested: bool = True

    @property
    def is_overdue(self) -> bool:
        if not self.response_deadline:
            return False
        try:
            deadline = datetime.strptime(self.response_deadline, "%Y-%m-%d")
            return datetime.now() > deadline and self.status not in ("responded",)
        except ValueError:
            return False

    @property
    def days_until_deadline(self) -> int:
        if not self.response_deadline:
            return -1
        try:
            deadline = datetime.strptime(self.response_deadline, "%Y-%m-%d")
            return (deadline - datetime.now()).days
        except ValueError:
            return -1


